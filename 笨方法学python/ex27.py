'''
逻辑术语：
and 与
or 或
not 非
!= 不等于
== 等于
>= 大于等于
<= 小于等于
True 真
False 假
'''

print(12 < 9)           # False
print(77 and 87)        # 87
print(7 or 8)           # 7
print(not 7)            # False
print(not(7 and 8))     # False
print(not(7 or 8))      # False
print(7 and 8 or 9)     # 8
print(7 or 8 and 9)     # 7