'''
关键词
and   del  from  not  while
as  elif   global   or   with
assert  else  if  pass  yield
break  except  import  print
class  exec  in  raise  in
continue  finally  is  return
def  for  lambda  try  

as: 创建别名，与 import 一起使用。
global: 声明全局变量。
nonlocal: 声明非局部变量。
pass: 空语句，不做任何事情，用于保持程序结构的完整性。
yield: 用于定义生成器。
assert: 用于调试，检查条件是否为真。
except: try 语句的一部分，用于捕获异常。
exec: 用于执行存储在字符串或文件中的 Python 语句。
raise: 抛出异常。
finally: 无论 try 块是否有异常都会执行的代码块。
is: 检查两个操作数的身份是否相同，即它们是否是同一个对象。
try: 用于异常处理。
'''


