'''
字符串格式化
%d  整数  %5d  至少5个字符宽度
%i  整数
%o  八进制整数
%x  十六进制整数
%X  十六进制整数（大写字母）
%u  无符号整数
%e  指数（基底写为e）
%E  指数（基底写为E）
%f  浮点数
%F  浮点数（与上相同）
%g  指数（e）或浮点数（根据显示长度）
%G  指数（E）或浮点数（根据显示长度）
%c  字符及其ASCII码
%r  字符串（repr()）
%s  字符串（str()）
%%  字符"%"
'''

